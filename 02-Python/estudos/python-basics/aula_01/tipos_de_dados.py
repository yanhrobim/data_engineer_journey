# Para entender melhor sobre tipos de dados, precisamos entender melhor o que é uma váriavel:
# De forma clara e simples, uma váriavel é algo que armazena um valor dentro do código.
# Quando utilizamos váriaveis em nossos sistemas, estamos consequentemente armazenando dados e salvando-os na memória RAM.

# 1. Inteiros (Int)

numero = 10     # Tipo Int. (Valor númerico inteiro sem casas decimais)


# 2. Números de ponto flutuante. (Float)

numero_decimal = 10.5     # Tipo Float. (Valor númerico com casas decimais)


# 3. String. (str)

texto = "Olá" # Tipo String. (Texto)


# 4. True ou False. (Bool)

verdadeiro = True # Tipo Boolean. (Valor verdadeiro ou falso, True para verdadeiro, False para Falso.)


